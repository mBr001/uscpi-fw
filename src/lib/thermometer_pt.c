FP_2_14_t Pt_TtoR(temp_1_20_t T)
{
    uint16_t idx;
    uint8_t fract;
    FP_2_14_t R1, R_step;

    if (T < (temp_1_20_t)(PT_TABLE_OFFSET * PT_CENTIGRADE))
        return 0;
    T -= PT_TABLE_OFFSET * PT_CENTIGRADE;

    idx = T / PT_TABLE_CTGS_PER_STEP;
    if (idx >= sizeof(Pt_T2R_table))
        return -1;
    fract = T % PT_TABLE_CTGS_PER_STEP;
    R1 = pgm_read_word(&Pt_T2R_table[idx]);

    R_step = pgm_read_word(&Pt_T2R_table[idx + 1]) - R1;
    
    return R1 + (R_step * fract / PT_CENTIGRADE);
}

temp_1_20_t Pt_RtoT(FP_2_14_t R)
{
    temp_1_20_t T;
    FP_2_14_t R_1, R_step;
    uint16_t idx;
    uint16_t step;
    
    idx = sizeof(Pt_T2R_table) / 2 - 1;
    step = sizeof(Pt_T2R_table) / 4;
    
    while (step) {
        R_1 = pgm_read_word(idx);
        if (R_1 >= R)
            idx -= step;
        else
            idx += step;
        step /= 2;
    };

    while (R_1 > R)
        R_1 = pgm_read_word(--idx);

    R_step = pgm_read_word(++idx) - R_1;

    T = ((idx * PT_TABLE_STEP) + PT_TABLE_OFFSET) * PT_CENTIGRADE + 
            (R - R_1) * PT_TABLE_CTGS_PER_STEP / R_step;

    return T;
}


