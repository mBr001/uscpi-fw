FP_2_14_t Pt_TtoR(temp_1_20_t T)
{
    uint16_t idx;
    uint8_t fract;
    FP_2_14_t Rlo, R_step;

    if (T < (temp_1_20_t)(PT_TABLE_OFFSET * PT_CENTIGRADE))
        return 0;
    T -= PT_TABLE_OFFSET * PT_CENTIGRADE;

    idx = T / PT_TABLE_CTGS_PER_STEP;
    if (idx >= sizeof(Pt_TtoR_table))
        return -1;
    fract = T % PT_TABLE_CTGS_PER_STEP;

    Rlo = pgm_read_word(&Pt_TtoR_table[idx]);
    R_step = pgm_read_word(&Pt_TtoR_table[idx + 1]) - Rlo;
    
    return Rlo + (R_step * fract / PT_CENTIGRADE);
}

temp_1_20_t Pt_RtoT(FP_2_14_t R)
{
    temp_1_20_t T;
    FP_2_14_t Rlo, R_step;
    uint16_t idx;
    uint16_t step;

    if (R < PT_TTOR_TABLE_MIN)
        return 0;
    if (R >= PT_TTOR_TABLE_MAX)
        return 0xffff;

    idx = sizeof(Pt_TtoR_table) / 2 - 1;
    step = sizeof(Pt_TtoR_table) / 4;
    
    while (step) {
        Rlo = pgm_read_word(&Pt_TtoR_table[idx]);
        if (Rlo >= R)
            idx -= step;
        else
            idx += step;
        step /= 2;
    };

    while (Rlo <= R)
        Rlo = pgm_read_word(&Pt_TtoR_table[++idx]);

    while (Rlo > R)
        Rlo = pgm_read_word(&Pt_TtoR_table[--idx]);

    R_step = pgm_read_word(&Pt_TtoR_table[idx + 1]) - Rlo;

    T = ((idx * PT_TABLE_STEP) + PT_TABLE_OFFSET) * PT_CENTIGRADE + 
            ((R - Rlo) * PT_TABLE_CTGS_PER_STEP + R_step / 2) / R_step;

    return T;
}

